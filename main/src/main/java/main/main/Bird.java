package main.main;

public class Bird {
    private int x;
    private int y;
    public Bird(){
        x = 200;
        y = 150;
    }
    public void setX(int x){
        this.x = x;
    }
    public void setY(int y){
        this.y = y;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
}
